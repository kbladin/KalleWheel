#!/bin/sh
#
# run-psjs-exec.sh
#
#  This script is a simple wrapper to be used on a mac with the
#  xtools xexec.js script.
#
#  For these instructions <CS2 Folder> should be replaced by the directory
#  where PSCS2 is installed.
#
#  To install, copy this script to your <CS2 Folder>/Presets/Scripts directory
#  and make it executable. You can do this from a terminal window by running
#  this command:
#     chmod 755 <CS2 Folder>/Presets/Scripts/run-psjs-exec.sh
#
chmod 755 /tmp/psjs-exec.sh
/tmp/psjs-exec.sh
